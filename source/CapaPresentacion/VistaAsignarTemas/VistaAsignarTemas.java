    // lista de libros de la biblioteca, vista izquierda
    public void updateListBooks()
    {
        ArrayList<String[]> list = controlPre.getAllLibros();
        String[] libro = new String[list.size()];
        int i = 0;
        for (String[] s : list)
        {
            libro[i] = s[2];
            ++i;
        }
        listLibros.setListData(libro);
    }

    // dado un id de libro, carga los temas asignados de este libro, vista derecha
    public void loadTemasAsignados(String Id) throws IOException
    {
        int id = Integer.parseInt(Id);
        ArrayList<String> list = new ArrayList<String>();
        list = controlPre.getTemas(id);
        String temas[] = list.toArray(new String[list.size()]);
        listTemas.setListData(temas);
    }

    public void loadBook(String idL, String nombreT) throws IOException
    {
        controlPre.asignarTemaLibro(idL, nombreT);
        loadTemasAsignados(idL);
    }




