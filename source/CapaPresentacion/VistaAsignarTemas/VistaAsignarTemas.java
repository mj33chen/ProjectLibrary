    private void listLibrosAncestorAdded (javax.swing.event.AncestorEvent evt) throws IOException
    {
        this.updateListBooks();
        String id = listLibros.getSelectedValue().toString();
        this.loadTemasAsignados(id);
    }

    private void buttonAsignActionPerformed (java.awt.event.ActionEvent evt) throws IOException
    {
        String idLibro = listLibros.getSelectedValue().toString();
        String nombreTema = comboBoxThemes.getSelectedItem().toString();

        if(! controlPre.temaRepetido(idLibro, nombreTema))
            this.loadBook(idLibro, nombreTema);
        else
        {
            JOptionPane.showMessageDialog(this, "ERROR: El tema ya esta asignado :( ", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

    }

    private void selectThemeAncestorAdded (javax.swing.event.AncestorEvent evt)
    {
        ArrayList<String> temas = controlPre.getAllThemes();
        comboBoxThemes.removeAllItems();
        for (String s : temas)
        {
            comboBoxThemes.addItem(s);
        }
        if (tema != null) {
            comboBoxThemes.setSelectedItem(tema);
        }
    }

