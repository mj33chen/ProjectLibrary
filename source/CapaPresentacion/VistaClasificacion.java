    private void saveClasificacionActionPerformed(java.awt.event.ActionEvent evt)
    {
        if(editNombre.getText().equals("") || (controlPre.esEmptyClasifi()))
        {
            JOptionPane.showMessageDialog(this, "ERROR: Te faltan datos ): ", "Error", JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            //controlPre.activeView("left", "crearBiblioteca");
            rebootLabelandList();
        }
    }

    private void buttonCreateActionPerformed(java.awt.event.ActionEvent evt)
    {
        if(! controlPre.esEmptyClasifi())
        {
            vistaTema.setMode(true);
            //controlPre.activeView("left", "crearTema");
        }
        else
            buttonCreate.setEnabled(false);
    }

    private void buttonModifyActionPerformed(java.awt.event.ActionEvent evt)  throws IOException
    {
        if(! controlPre.esEmptyClasifi())
        {
            String nombreTema = listTemas.getSelectedValue().toString();
            if(nombreTema.equals("General"))
            {
                JOptionPane.showMessageDialog(this, "ERROR: No se puede modificar tema General ", "Error", JOptionPane.WARNING_MESSAGE);
                return;
            }
            vistaTema.loadTema(nombreTema);
            vistaTema.setMode(false);
            //controlPre.activeView("left", "crearTema");
        }
        else
            buttonModify.setEnabled(false);
    }

    private void buttonDeleteActionPerformed(java.awt.event.ActionEvent evt) throws IOException
    {
        String nombreTema = listTemas.getSelectedValue().toString();
        controlPre.eliminarTema(nombreTema);
        this.updateListThemes();
    }

    private void listTemasAncestorAdded(javax.swing.event.AncestorEvent evt)
    {
        this.updateListThemes();
    }

