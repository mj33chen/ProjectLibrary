    public void updateListThemes()
    {
        ArrayList<String> list = controlPre.getAllThemes();
        String[] temas = new String [list.size()];
        int i = 0;
        for(String s : list)
        {
            temas[i] = s;
            ++i;
        }
        listTemas.setListData(temas);
    }

